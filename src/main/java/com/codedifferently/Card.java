package com.codedifferently;

public class Card {

    private Integer rank;
    private Integer suit;

    public Integer getSuit() {
        return suit;
    }
    public void setSuit(int s)
    {
        this.suit = s;
    }
    public Integer getRank()
    {
        return rank;
    }
    public void setRank(int r)
    {
        this.rank = r;
    }
    
    
}