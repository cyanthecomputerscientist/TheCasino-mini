package com.codedifferently;

public class Card {

    private Integer value;
    private Integer suit;

    public Integer getSuit() {
        return suit;
    }
    public void setSuit(int s)
    {
        this.suit = s;
    }
    public Integer getValue()
    {
        return value;
    }
    public void setValue(int v)
    {
        this.value = v;
    }
    
    
}